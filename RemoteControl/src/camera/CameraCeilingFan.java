package camera;

public class CameraCeilingFan {
	private boolean isCeilingFanHigh = false;
	private boolean isCeilingFanMedium = false;
	private boolean isCeilingFanLow = false;
	private boolean isCeilingFanOff = false;

	public boolean isCeilingFanHigh() {
		return isCeilingFanHigh;
	}

	public void setCeilingFanHigh(boolean isCeilingFanHigh) {
		this.isCeilingFanHigh = isCeilingFanHigh;
	}

	public boolean isCeilingFanMedium() {
		return isCeilingFanMedium;
	}

	public void setCeilingFanMedium(boolean isCeilingFanMedium) {
		this.isCeilingFanMedium = isCeilingFanMedium;
	}

	public boolean isCeilingFanLow() {
		return isCeilingFanLow;
	}

	public void setCeilingFanLow(boolean isCeilingFanLow) {
		this.isCeilingFanLow = isCeilingFanLow;
	}

	public boolean isCeilingFanOff() {
		return isCeilingFanOff;
	}

	public void setCeilingFanOff(boolean isCeilingFanOff) {
		this.isCeilingFanOff = isCeilingFanOff;
	}

}
